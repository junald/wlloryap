    private void initScreen(){
        if(dependent != null){
            this.txtFirstName.setText(dependent.getFirstName());
            this.txtLastName.setText(dependent.getLastName());
            this.txtMiddleName.setText(dependent.getMiddleName());
            this.txtDateOfBirth.setDate(dependent.getDateOfBirth());

            if(dependent.getGender() == 'F'){
                this.rbFemale.setSelected(true);                
            }else{
                this.rbMale.setSelected(true);
            }

            this.comboRelationship.setSelectedItem(dependent.getRelationship());
        }
        
    }

    private void saveScreen(){
            dependent.setFirstName(this.txtFirstName.getText());
            dependent.setLastName(this.txtLastName.getText());
            dependent.setMiddleName(this.txtMiddleName.getText());
            dependent.setDateOfBirth(this.txtDateOfBirth.getDate());
            if(this.rbFemale.isSelected()){
                dependent.setGender('F');
            }else{
                dependent.setGender('M');
            }
            dependent.setRelationship( this.comboRelationship.getSelectedItem().toString());
    }
